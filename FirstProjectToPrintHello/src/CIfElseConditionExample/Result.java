package CIfElseConditionExample;

public class Result {

	public static void main(String[] args) {
		int math = 80;
		int science = 90;
		int history = 60;

		if (math >= 60) {
			System.out.println("pass in math");
		}

		 if (science >= 60) {
			System.out.println("pass in science");
		}

		 if (history >= 60) {
			System.out.println("Pass in history");
		}
		else {
			System.out.println("Fail");
		}
		
	}

}
